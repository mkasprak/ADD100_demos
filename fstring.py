""" 
    Introducing Formatting with fstring
    Docstring example
"""

# Setting up variables
var_1 = 2.3345
var_2 = 2.24
var_3 = "Meri"

# Printing with fstring
print(f"Hello, my name is {var_3}")
print(f"We are going to format. var_1: {var_1:.2f}")
print(f"{var_2:.1f}")

